/**
 * Tipo da declaração da Variável
 * - let
 * 
 * Muito usado para declaração de variáveis mutáveis.
 * Substituto do tipo var.
 */
let variavel_mutavel = 'Mutável'
variavel_mutavel = 'Mutável - Alterado'

/**
 * Tipo da declaração da Variável
 * - const
 * 
 * Muito usado para declaração de variáveis imutáveis.
 * Criado para declarar constantes.
 */
const VARIAVEL_IMUTAVEL = 1
// variavel_imutavel = 1+1 (Isto não ocorre, pois a variável é imutável).

/**
 * O que a convenção nos diz sobre variáveis
 * - Não podem iniciar com numeros, @ ou !.
 * - Não podem ser declaradas com nomes de palavras reservadas.
 * - Pode terminar com numero no nome.
 * - Pode começar com $ ou _.
 * - Indica-se utilizarmos o padrão camelCase
 *   - let nomeDaVariavel = ''
 */